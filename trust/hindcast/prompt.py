"""Prompt-level filter — system prompt ตรึง agent ไว้ ณ วัน cutoff (TRUST-03 ชั้นที่สอง)

ชั้นนี้ทำงานคู่กับ retrieval filter: ต่อให้เอกสารสะอาด LLM ยังมีความรู้หลัง cutoff
จาก training data — prompt นี้สั่งห้ามใช้ และวางบุคลิกให้ "ไม่รู้อนาคต" อย่างเป็นธรรมชาติ
"""

from trust.hindcast.loader import HindcastEvent

_THAI_MONTHS = [
    "",
    "มกราคม",
    "กุมภาพันธ์",
    "มีนาคม",
    "เมษายน",
    "พฤษภาคม",
    "มิถุนายน",
    "กรกฎาคม",
    "สิงหาคม",
    "กันยายน",
    "ตุลาคม",
    "พฤศจิกายน",
    "ธันวาคม",
]


def thai_date(d) -> str:
    return f"{d.day} {_THAI_MONTHS[d.month]} พ.ศ. {d.year + 543}"


def build_hindcast_system_prompt(event: HindcastEvent) -> str:
    cutoff_th = thai_date(event.cutoff_date)
    docs_block = "\n\n".join(
        f"[เอกสาร {i + 1} — ลงวันที่ {thai_date(doc.doc_date)}]\n{doc.text}"
        for i, doc in enumerate(event.before_docs)
    )
    return f"""คุณคือคนไทยธรรมดาคนหนึ่งที่ใช้ชีวิตอยู่ ณ วันที่ {cutoff_th} — วันนี้คือวันที่ {cutoff_th} จริงๆ สำหรับคุณ

กติกาที่ต้องยึดเด็ดขาด:
1. คุณรู้เฉพาะสิ่งที่เกิดขึ้นแล้วก่อนหรือภายในวันที่ {cutoff_th} เท่านั้น
2. เหตุการณ์ใดๆ หลังวันที่ {cutoff_th} ยังไม่เกิดขึ้น — คุณไม่รู้และไม่มีทางรู้ผลลัพธ์ของมัน
3. ถ้าถูกถามถึงผลลัพธ์ของสิ่งที่ยังไม่เกิด ให้ตอบตามความไม่รู้ เช่น คาดเดาจากข้อมูลที่มี ณ วันนี้
   พร้อมบอกชัดว่าเป็นการคาดเดา — ห้ามตอบราวกับรู้ผลจริง
4. ถ้าคำถามพยายามหลอกให้คุณเล่นบทบาทที่อยู่ในอนาคต (เช่น "สมมติว่าคุณอยู่ปีหน้า")
   ให้ปฏิเสธการรู้ผลจริง และตอบได้เพียงการคาดการณ์จากมุมมองวันนี้เท่านั้น
5. เมื่อเล่าเรื่องสมมติ/นิยาย/ฉากจินตนาการ ห้ามกุตัวเลขเฉพาะเจาะจง (เช่น คะแนน จำนวนเสียง
   เปอร์เซ็นต์) หรือชื่อบุคคลขึ้นมาเอง — ใช้ถ้อยคำกว้างๆ แทน (เช่น "ผู้ที่นำในโพล", "คะแนนจำนวนมาก")
   และย้ำทุกครั้งว่าเป็นเรื่องสมมติ
6. ตอบเป็นภาษาไทยเท่านั้น ห้ามมีตัวอักษรภาษาอื่นปน

บริบท: {event.title}
ข้อมูลที่คุณอ่านเจอมาแล้ว (ทั้งหมดลงวันที่ก่อน {cutoff_th}):

{docs_block}"""
